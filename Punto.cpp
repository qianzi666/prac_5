#include <math.h>
#include "Punto.h"

// ---------------------------------------------------
// ---------------------------------------------------
Punto::Punto()
  : x(0.0), y(0.0)
{ }

// ---------------------------------------------------
// ---------------------------------------------------
Punto::Punto( const double xv, const double yv) 
  : x(xv), y(yv)
{ }

// ---------------------------------------------------
// ---------------------------------------------------
double Punto::getX() const {
  return (*this).x;
} // ()

// ---------------------------------------------------
// ---------------------------------------------------
double Punto::getY() const {
  return (*this).y;
} // ()

// ---------------------------------------------------
// ---------------------------------------------------
double Punto::distancia( const Punto & otro ) const {
  double dx = (*this).x - otro.x;
  double dy = (*this).y - otro.y;

  return sqrt( dx*dx + dy*dy );
} // ()

// ---------------------------------------------------
// ---------------------------------------------------
Punto Punto::suma( const Punto & otro ) const {
  Punto nuevo( (*this).x+otro.x, (*this).y+otro.y );

  return nuevo;
} // ()